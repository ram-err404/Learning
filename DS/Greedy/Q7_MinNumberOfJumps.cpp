// Function to return minimum number of jumps to end of array

class Solution{
  public:
    int minJumps(int arr[], int n){
        // Your code here
        int count=0;
        int i=0;
        while(i<n) {
            i += arr[i];
            count++;
        }
        return count;
    }
};


